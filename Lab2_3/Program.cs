using System;

class Program
{
    static void Main()
    {
        int count = 0; // Счетчик четырехзначных чисел
        int f0 = 1; // Первый член ряда Фибоначчи
        int f1 = 1; // Второй член ряда Фибоначчи
        int fn = 0; // Третий член ряда Фибоначчи

        // Генерация чисел Фибоначчи до тех пор, пока не достигнем четырехзначных чисел
        while (fn < 10000)
        {
            fn = f0 + f1; // Вычисляем следующий член ряда

            // Проверяем, является ли текущий член четырехзначным
            if (fn >= 1000 && fn < 10000)
            {
                count++;
            }

            // Сдвигаем числа для следующей итерации
            f0 = f1;
            f1 = fn;
        }

        Console.WriteLine($"Количество четырехзначных чисел в ряде Фибоначчи: {count}");
    }
}
